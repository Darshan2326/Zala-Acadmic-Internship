public class RelationalOperators {
    
    public static void relationalOperations(int a, int b) {
        System.out.println("a < b: " + (a < b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a >= b: " + (a >= b));
    }
    
    public static void main(String[] args) {
        relationalOperations(10, 20);
    }
}
